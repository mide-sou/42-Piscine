#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	c;
	int	d;

	d = 1;
	c = 0;
	while (d < argc)
	{
		while (argv[d][c] != '\0')
		{
			write(1, &argv[d][c], 1);
			c++;
		}
		write(1, "\n", 1);
		d++;
		c = 0;
	}
}
